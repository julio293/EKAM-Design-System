/** Inline EKAM nav glyph, recoloured via `color` (currentColor). */
export function NavIcon({ name, size = 20, color = 'currentColor', style }) {
  const g = NAV_ICONS[name];
  if (!g) return null;
  return (
    <svg width={size} height={size} viewBox={g.vb} fill={color} preserveAspectRatio="xMidYMid meet" aria-hidden style={style}>
      <path d={g.d} />
    </svg>
  );
}

// Default EKAM destinations. Hindi labels are overridable via `items`.
export const TAB_BAR_ITEMS = [
  { key: 'today', label: 'Today', hindi: 'आज', icon: 'today' },
  { key: 'stay', label: 'Stay', hindi: 'निवास', icon: 'stay' },
  { key: 'explore', label: 'Explore', hindi: 'खोज', icon: 'explore' },
  { key: 'cabin', label: 'Cabin', hindi: 'कुटीर', icon: 'cabin' },
  { key: 'connect', label: 'Connect', hindi: 'संपर्क', icon: 'connect' },
];

export default function TabBar({
  items = TAB_BAR_ITEMS,
  value,
  defaultValue,
  onChange,
  showHindi = true,
  homeIndicator = true,
  style,
  ...rest
}) {
  const isControlled = value !== undefined;
  const [internal, setInternal] = useState(defaultValue ?? items[0]?.key);
  const active = isControlled ? value : internal;

  const select = (k) => {
    if (!isControlled) setInternal(k);
    onChange?.(k);
  };

  return (
    <nav
      style={{
        position: 'relative',
        display: 'flex',
        alignItems: 'flex-start',
        width: 393,
        maxWidth: '100%',
        background: palette.cream,
        paddingBottom: 32, // home-indicator safe area
        overflow: 'hidden',
        boxSizing: 'border-box',
        ...style,
      }}
      {...rest}
    >
      {items.map((item) => {
        const selected = item.key === active;
        const color = selected ? palette.bindu : palette.moss;
        return (
          <button
            key={item.key}
            type="button"
            aria-current={selected ? 'page' : undefined}
            onClick={() => select(item.key)}
            style={{
              flex: '1 1 0',
              minWidth: 0,
              display: 'flex',
              flexDirection: 'column',
              alignItems: 'center',
              gap: 4,
              padding: '8px 4px',
              border: 'none',
              borderTop: `1.5px solid ${selected ? palette.bindu : 'transparent'}`,
              background: 'transparent',
              cursor: 'pointer',
              opacity: selected ? 1 : 0.7,
            }}
          >
            {item.icon && (typeof item.icon === 'string' ? <NavIcon name={item.icon} color={color} /> : item.icon)}
            {showHindi && item.hindi && (
              <span style={{ fontFamily: "'Inter', system-ui, sans-serif", fontWeight: 400, fontSize: 11, lineHeight: '13.5px', color, textAlign: 'center' }}>
                {item.hindi}
              </span>
            )}
            <span
              style={{
                fontFamily: "'Inter', system-ui, sans-serif",
                fontWeight: 600,
                fontSize: 10,
                lineHeight: 1,
                letterSpacing: '2.5px',
                textTransform: 'uppercase',
                color,
                textAlign: 'center',
              }}
            >
              {item.label}
            </span>
          </button>
        );
      })}

      {homeIndicator && (
        <span
          aria-hidden
          style={{ position: 'absolute', left: '50%', transform: 'translateX(-50%)', bottom: 8, width: 145, height: 4, borderRadius: 999, background: palette.ink }}
        />
      )}
    </nav>
  );
}
